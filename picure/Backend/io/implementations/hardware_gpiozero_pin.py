import gpiozero
from picure.Backend.io.prototypes.proto_hardware import HardwareProto


class GPIOZeroPin(HardwareProto):
    def __init__(self, pin, name):
        self.name = name
        self.DEV = gpiozero.OutputDevice(pin, active_high=True)

    def on(self):
        self.DEV.on()

    def off(self):
        self.DEV.off()

    def toggle(self):
        self.DEV.toggle()

    def get(self):
        self.DEV.value
